def add_line_to_voxel(voxel_grid, start_point, end_point):
    """
    Add a straight line to a voxel grid using Bresenham's 3D line algorithm.

    This function draws a straight line between two points in a 3D voxel grid
    by setting all voxels intersected by the line to True/1. It uses an optimized
    3D implementation of Bresenham's line algorithm, which selects the optimal
    driving axis to minimize errors and ensure the most accurate line representation
    in voxel space.

    The algorithm works by:
    1. Determining the driving axis (the axis with the largest coordinate difference)
    2. Stepping along that axis while conditionally stepping along the other axes
       based on error accumulation parameters
    3. Setting each visited voxel to True (occupied)
    4. Ensuring the line is continuous and follows the ideal straight line as
       closely as possible in discretized space

    Parameters
    ----------
    voxel_grid : numpy.ndarray
        3D binary array representing the voxel grid. Can be of any dtype, but
        typically boolean or integer. The grid is modified in-place by setting
        line voxels to True/1. Assumes a cubic grid (all dimensions equal).

    start_point : tuple
        (x, y, z) coordinates of the line start point. Coordinates should be
        within the grid boundaries for the line to be visible, but the function
        includes bounds checking and safely handles out-of-bounds points.

    end_point : tuple
        (x, y, z) coordinates of the line end point. Like start_point, coordinates
        are bounds-checked during drawing.

    Returns
    -------
    numpy.ndarray
        The modified voxel grid (same object as input, modified in-place) with
        all voxels along the line set to True/1.

    Notes
    -----
    **Bresenham's 3D Line Algorithm:**

    This is a generalization of Bresenham's famous 2D line algorithm to three
    dimensions. The key concepts are:

    1. **Driving Axis Selection**: The axis with the largest absolute difference
       between start and end points becomes the "driving axis" (the one we step
       along most frequently). This ensures the line has the correct length and
       minimizes discretization error.

    2. **Error Accumulation**: For the non-driving axes, we maintain error
       terms (p1, p2) that determine when to take a step along that axis. When
       the accumulated error exceeds a threshold, we step and adjust the error.

    3. **Integer Arithmetic**: The algorithm uses only integer arithmetic
       (multiplications by 2) for efficiency and precision, avoiding floating-point
       operations.

    4. **Symmetry and Direction**: The algorithm handles both positive and negative
       directions gracefully using direction signs (xs, ys, zs).

    **Algorithm Variants:**

    The implementation handles three cases based on the driving axis:

    - **X-driven** (dx ≥ dy and dx ≥ dz): Steps along X, conditionally along Y and Z
    - **Y-driven** (dy ≥ dx and dy ≥ dz): Steps along Y, conditionally along X and Z
    - **Z-driven** (dz ≥ dx and dz ≥ dy): Steps along Z, conditionally along X and Y

    **Why Use This Algorithm?**

    - **Accuracy**: Produces the closest possible approximation to a straight line
      in discretized space
    - **Efficiency**: Uses only integer arithmetic and simple comparisons
    - **No Gaps**: Ensures connected, continuous lines without holes
    - **Deterministic**: Same inputs always produce same output
    - **Bounds-Safe**: Includes boundary checking to prevent index errors

    **Applications:**

    - Adding anatomical landmarks to medical volumes
    - Drawing measurement guides or reference axes
    - Creating synthetic test data with known features
    - Simulating fractures, cracks, or linear features
    - Building 3D wireframes from line segments
    - Adding surgical planning markers

    Examples
    --------
    Basic line drawing in an empty grid:

    >>> import numpy as np
    >>>
    >>> # Create an empty 64x64x64 voxel grid
    >>> grid = np.zeros((64, 64, 64), dtype=bool)
    >>>
    >>> # Draw a diagonal line from corner to corner
    >>> grid = add_line_to_voxel(grid, (0, 0, 0), (63, 63, 63))
    >>> print(f"Number of voxels in line: {np.sum(grid)}")
    Number of voxels in line: 64

    Drawing multiple lines to create a 3D structure:

    >>> # Create a simple 3D cross
    >>> grid = np.zeros((32, 32, 32), dtype=bool)
    >>>
    >>> # Horizontal line along X-axis
    >>> grid = add_line_to_voxel(grid, (5, 16, 16), (26, 16, 16))
    >>>
    >>> # Horizontal line along Y-axis
    >>> grid = add_line_to_voxel(grid, (16, 5, 16), (16, 26, 16))
    >>>
    >>> # Vertical line along Z-axis
    >>> grid = add_line_to_voxel(grid, (16, 16, 5), (16, 16, 26))
    >>>
    >>> # Draw a diagonal connector
    >>> grid = add_line_to_voxel(grid, (26, 16, 16), (16, 26, 16))
    >>>
    >>> print(f"Total occupied voxels: {np.sum(grid)}")

    Technical Details
    -----------------
    - **Complexity**: O(L) where L is the number of steps along the driving axis
    - **Memory**: Modifies grid in-place, no significant additional memory
    - **Precision**: Uses only integer arithmetic for exact results
    - **Boundary Safety**: Includes bounds checking for each voxel
    - **Axis Handling**: Automatically selects optimal driving axis

    **Line Properties:**
    - Produces a 6-connected line (voxels share faces)
    - No diagonal jumps (ensures connectivity)
    - Symmetric for swapped start/end points

    Requirements
    ------------
    - numpy (imported as np)

    See Also
    --------
    scipy.ndimage.draw.line_nd : SciPy's n-dimensional line drawing
    skimage.draw.line_aa : Anti-aliased line drawing in 2D
    binary_dilation : Morphological operation to thicken lines

    Warnings
    --------
    - **In-Place Modification**: The function modifies the input grid directly.
      If you need to preserve the original, pass a copy: `result = add_line_to_voxel(grid.copy(), start, end)`

    - **Grid Shape Assumption**: Assumes a cubic grid where all dimensions are equal.
      While the algorithm will work with non-cubic grids, the indexing assumes
      the same size in all dimensions for bounds checking.

    - **Integer Coordinates**: Coordinates are used as integers. Floating-point
      coordinates will be truncated to integers (through tuple indexing).

    - **Out-of-Bounds Points**: Points outside the grid are handled gracefully
      (only in-bounds voxels are set), but the line may be partially visible.

    - **Line Thickness**: This draws a 1-voxel thick line. For thicker lines,
      combine with morphological dilation.

    - **Performance for Long Lines**: For very long lines (approaching grid
      dimensions), the algorithm performs O(grid_size) operations, which is
      optimal for this task.

    - **Boolean vs Integer Output**: The function sets voxels to True (for boolean
      arrays) or 1 (for integer arrays). Ensure your array type is compatible.
    """
    x1, y1, z1 = start_point
    x2, y2, z2 = end_point

    # Get grid dimensions
    size = voxel_grid.shape[0]

    # 3D Bresenham's line algorithm
    dx = np.abs(x2 - x1)
    dy = np.abs(y2 - y1)
    dz = np.abs(z2 - z1)

    xs = 1 if x2 > x1 else -1
    ys = 1 if y2 > y1 else -1
    zs = 1 if z2 > z1 else -1

    # Driving axis is X
    if dx >= dy and dx >= dz:
        p1 = 2 * dy - dx
        p2 = 2 * dz - dx
        while x1 != x2:
            if 0 <= x1 < size and 0 <= y1 < size and 0 <= z1 < size:
                voxel_grid[x1, y1, z1] = True
            x1 += xs
            if p1 >= 0:
                y1 += ys
                p1 -= 2 * dx
            if p2 >= 0:
                z1 += zs
                p2 -= 2 * dx
            p1 += 2 * dy
            p2 += 2 * dz
    # Driving axis is Y
    elif dy >= dx and dy >= dz:
        p1 = 2 * dx - dy
        p2 = 2 * dz - dy
        while y1 != y2:
            if 0 <= x1 < size and 0 <= y1 < size and 0 <= z1 < size:
                voxel_grid[x1, y1, z1] = True
            y1 += ys
            if p1 >= 0:
                x1 += xs
                p1 -= 2 * dy
            if p2 >= 0:
                z1 += zs
                p2 -= 2 * dy
            p1 += 2 * dx
            p2 += 2 * dz
    # Driving axis is Z
    else:
        p1 = 2 * dy - dz
        p2 = 2 * dx - dz
        while z1 != z2:
            if 0 <= x1 < size and 0 <= y1 < size and 0 <= z1 < size:
                voxel_grid[x1, y1, z1] = True
            z1 += zs
            if p1 >= 0:
                y1 += ys
                p1 -= 2 * dz
            if p2 >= 0:
                x1 += xs
                p2 -= 2 * dz
            p1 += 2 * dy
            p2 += 2 * dx

    # Set the endpoint
    if 0 <= x2 < size and 0 <= y2 < size and 0 <= z2 < size:
        voxel_grid[x2, y2, z2] = True

    return voxel_grid

def find_closest_to_origin(vectors):
    """
    Returns the vector in the input array that is closest to the origin (0,0,0)

    Parameters:
    vectors : numpy.ndarray
        Array of shape (n,3) containing n 3D vectors

    Returns:
    numpy.ndarray
        The vector closest to the origin
    int
        Index of the closest vector in the original array
    """
    # Calculate Euclidean distances from origin for all vectors
    distances = np.linalg.norm(vectors, axis=1)

    # Find the index of the minimum distance
    min_idx = np.argmin(distances)

    # Return the closest vector and its index
    return vectors[min_idx], min_idx

# Alternative version that returns only the vector (without index)
def get_closest_to_origin(vectors):
    """
    Returns only the vector closest to the origin
    """
    distances = np.linalg.norm(vectors, axis=1)
    return vectors[np.argmin(distances)]

