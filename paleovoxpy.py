def save_voxel(voxel, path: str):
    """
    Save a 3D voxel array to a NumPy .npy file with error handling and user feedback.

    This function provides a simple, safe wrapper around NumPy's array saving
    functionality. It attempts to save a voxel grid to the specified file path
    and provides clear feedback about success or failure, making it ideal for
    batch processing, automated pipelines, and situations where robust error
    handling is important.

    The function:
    1. Attempts to save the voxel array using NumPy's `np.save`
    2. On success: Prints a confirmation message with the file path
    3. On failure: Catches the exception and prints the error message
    4. Always completes without raising exceptions (fail-safe)

    Parameters
    ----------
    voxel : numpy.ndarray
        The 3D voxel array to save. Typically a binary array (0s and 1s) from
        functions like `mesh_to_voxel`, but can be any NumPy array of any dtype.
        Common voxel array characteristics:
        - Shape: Usually (128, 128, 128) or (64, 64, 64)
        - Dtype: Often uint8, bool, or int8 for binary voxels
        - Values: 0 for empty, 1 for occupied (for binary grids)

    path : str
        File path where the voxel array will be saved. The function automatically
        appends the `.npy` extension if not present. Examples:
        - `"fossil_voxels"` → saves as `fossil_voxels.npy`
        - `"data/voxel_grid.npy"` → saves in `data` directory
        - `"output/experiment_001"` → saves as `experiment_001.npy`

        The directory must exist; NumPy will not create parent directories.

    Returns
    -------
    None
        The function does not return any value. Success or failure is communicated
        through print statements to the console.

    Notes
    -----
    **File Format: NumPy .npy**

    The `.npy` format is NumPy's native binary format for array storage.

    Examples
    --------
    Basic voxel saving:

    >>> import numpy as np
    >>>
    >>> # Create or obtain a voxel array
    >>> voxels = mesh_to_voxel("fossil.obj", dimensions=128)
    >>>
    >>> # Save it to a file
    >>> save_voxel(voxels, "fossil_voxels")
    Correctly saved: fossil_voxels.npy

    Saving with explicit .npy extension:

    >>> save_voxel(voxels, "output/fossil_voxels.npy")
    Correctly saved: output/fossil_voxels.npy

    Technical Details
    -----------------
    - **Dependency**: Requires NumPy library
    - **File Extension**: Automatically adds `.npy` if not present
    - **Error Handling**: Catches all exceptions (base Exception class)
    - **Output**: Prints to stdout (console)
    - **Return Value**: None (use file existence check to verify success)
    - **Performance**: Fast, memory-mapped I/O; speed depends on array size

    Requirements
    ------------
    - numpy (imported as np)

    See Also
    --------
    np.save : The underlying NumPy function
    np.load : Loading saved .npy files
    np.savez : Save multiple arrays in compressed format
    mesh_to_voxel : Generate voxel arrays from meshes
    binary_dilation : Enhance voxel arrays before saving

    Warnings
    --------
    - **No Directory Creation**: The function does not create parent directories.
      If the directory in `path` doesn't exist, the save will fail with
      "No such file or directory" error.

    - **Overwrites Existing Files**: If a file already exists at `path.npy`, it will
      be silently overwritten without warning.
    """
    try:
        np.save(path, voxel)
        print('Correctly saved:', path)

    except Exception as e:
        print(e)
