import numpy as np
from scipy.ndimage import affine_transform, zoom, binary_dilation, binary_erosion

def deformation(voxel_array, compaction_factor=0.85,
                compaction_axis=2,
                preserve_binary=True, threshold=0.5):
    """
    Apply axial compaction deformation to a 3D binary voxel array.

    This function simulates geological or mechanical deformation by compressing a
    voxel grid along a specified axis while preserving the overall volume's
    dimensions through intelligent padding. It's particularly useful for:
    - Simulating sedimentary compaction in geological models
    - Creating augmented training data for 3D deep learning
    - Modeling anisotropic scaling in volumetric data
    - Preparing data for deformation-invariant analysis

    The deformation process works by:
    1. Resizing the voxel array along the specified axis using nearest-neighbor interpolation
    2. Centering the compressed volume within the original bounding box
    3. Maintaining binary nature through thresholding operations

    Parameters
    ----------
    voxel_array : numpy.ndarray
        Input 3D binary voxel array (values 0 and 1). Can be of any integer or boolean type.
        The array represents a volumetric occupancy grid to be deformed.

    compaction_factor : float, default=0.85
        Compression factor along the specified axis. Must be in range (0, 1.0]:
        - 1.0: No compression (identity transform)
        - 0.85: Compress to 85% of original length (default, typical for moderate compaction)
        - 0.5: Compress to 50% of original length (aggressive compaction)
        - 0.1: Extreme compression (may cause significant information loss)
        Values > 1.0 are not supported (use expansion functions instead).

    compaction_axis : int, default=2
        Axis along which to apply compaction:
        - 0: Z-axis (vertical/depth direction, common for sedimentary modeling)
        - 1: Y-axis (horizontal/width direction)
        - 2: X-axis (horizontal/length direction)
        The axis numbering follows numpy's 3D array convention (depth, height, width).

    preserve_binary : bool, default=True
        If True, ensures the output remains strictly binary (0 and 1 values) by applying
        thresholding after the deformation. If False, returns floating-point values
        from the interpolation (may contain intermediate values).

    threshold : float, default=0.5
        Threshold value for binary conversion when preserve_binary=True. Values > threshold
        become 1, values ≤ threshold become 0. Standard 0.5 works well for binary inputs;
        adjust if you need to control the binarization sensitivity.

    Returns
    -------
    numpy.ndarray
        Deformed voxel array with the same shape as the input. Values depend on
        preserve_binary:
        - If preserve_binary=True: Binary array (same dtype as input) with 0/1 values
        - If preserve_binary=False: Float array with values in range [0, 1]

    Notes
    -----
    **Deformation Mechanism:**

    The function implements a "squash and center" approach:

    1. **Compression**: Uses scipy.ndimage.zoom with order=0 (nearest neighbor)
       interpolation to compress the voxel grid along the specified axis. Nearest
       neighbor interpolation preserves the binary nature better than linear
       interpolation.

    2. **Centering**: Places the compressed volume in the center of the original
       bounding box, distributing empty space equally on both sides. This maintains
       spatial relationships and prevents boundary artifacts.

    3. **Binary Preservation**: Applies thresholding to ensure clean binary output,
       eliminating any floating-point artifacts from the interpolation process.

    **Why Use This Function?**

    - **Geological Modeling**: Simulate sedimentary compaction where layers compress
      vertically over time
    - **Data Augmentation**: Create deformed variants of training data to improve
      model robustness to shape variations
    - **Anisotropic Analysis**: Study how features change under directional scaling
    - **Volume Registration**: Correct for known anisotropic distortions in acquired data
    - **Physical Simulation**: Model material compression in simplified scenarios

    **Limitations:**

    - Preserves overall shape dimensions (padding with zeros) rather than shrinking
      the bounding box
    - Nearest-neighbor interpolation may cause stair-stepping artifacts at high
      compression ratios
    - No shearing or non-uniform deformation supported (use affine_transform for that)
    - Assumes binary input; non-binary inputs may produce unexpected results

    Examples
    --------
    Basic vertical compaction (simulating sediment burial):

    >>> # Load a voxelized fossil or geological layer
    >>> voxel_model = mesh_to_voxel("fossil.obj", dimensions=128)
    >>> print(f"Original shape: {voxel_model.shape}, occupied: {np.sum(voxel_model)}")
    Original shape: (128, 128, 128), occupied: 45231

    >>> # Apply 15% vertical compaction (typical for moderate burial)
    >>> compacted = deformation(voxel_model, compaction_factor=0.85, compaction_axis=0)
    >>> print(f"After compaction - occupied: {np.sum(compacted)}")
    After compaction - occupied: 45198

    Creating augmented training data:

    >>> # Generate multiple deformed versions for training data augmentation
    >>> original_voxels = mesh_to_voxel("training_model.obj")
    >>> augmented_dataset = [original_voxels]
    >>>
    >>> # Add variations with different compaction factors and axes
    >>> for axis in [0, 1, 2]:
    ...     for factor in [0.8, 0.9, 0.95]:
    ...         deformed = deformation(original_voxels,
    ...                                compaction_factor=factor,
    ...                                compaction_axis=axis)
    ...         augmented_dataset.append(deformed)
    >>> print(f"Dataset size: {len(augmented_dataset)} samples")

    Handling non-binary inputs with custom threshold:

    >>> # Input might have soft probabilities from a neural network
    >>> soft_voxels = np.random.rand(128, 128, 128)  # Values between 0 and 1
    >>> # Use lower threshold to preserve more structure
    >>> binary_result = deformation(soft_voxels, compaction_factor=0.8,
    ...                              threshold=0.3, preserve_binary=True)


    Integration with mesh reconstruction pipeline:

    >>> # Simulate deformed fossil and reconstruct
    >>> original = mesh_to_voxel("fossil.obj")
    >>> deformed_voxels = deformation(original, compaction_factor=0.75, compaction_axis=0)
    >>> reconstructed_mesh = high_quality_voxel_to_mesh(deformed_voxels, voxel_size=0.1)
    >>> # The mesh will show vertical compression
    >>> plot_meshes(reconstructed_mesh)

    Technical Details
    -----------------
    - **Interpolation**: Uses order=0 (nearest neighbor) to preserve binary nature
    - **Centering**: Symmetric padding distributes empty space equally on both sides
    - **Data Types**: Handles bool, int, and float inputs; returns requested type
    - **Performance**: O(n³) complexity, typically < 0.1 seconds for 128³ arrays
    - **Memory**: Creates intermediate float arrays, ~2-3x memory overhead

    Requirements
    ------------
    - numpy (imported as np)
    - scipy.ndimage (zoom function)

    See Also
    --------
    scipy.ndimage.zoom : Underlying resizing operation
    scipy.ndimage.affine_transform : More general geometric transformations
    binary_dilation, binary_erosion : Morphological operations for shape modification
    mesh_to_voxel : Generate initial voxel representation

    Warnings
    --------
    - **Information Loss**: Compression inevitably loses information, especially at
      low compaction factors (<0.7). Thin features may disappear entirely.

    - **Boundary Effects**: The centered padding assumes the object is roughly centered
      in the volume. Off-center objects may shift relative to boundaries.

    - **Binary Assumption**: The function works best with binary inputs. For
      probability maps or grayscale volumes, consider using preserve_binary=False
      and handle the results appropriately.

    - **Axis Convention**: Axis numbering (0,1,2) follows numpy convention, which
      may differ from visualization conventions. Verify by inspecting slices.

    - **Integer Division**: When original_shape[axis] * compaction_factor is not
      an integer, it's rounded down, potentially causing slight asymmetry in
      the centering.

    - **Extreme Compression**: Very low compaction factors (<0.3) may compress
      features to single voxel layers, making them difficult to distinguish.

    - **No Volume Conservation**: This function preserves the bounding box, not the
      volume. For volume-preserving deformation, you would need to expand other axes.
    """
    # Ensure input is binary and of integer type
    if voxel_array.dtype != np.bool_ and not np.all(np.isin(voxel_array, [0, 1])):
        pass
        print("Warning: Input array may not be binary. Converting to binary.")

    # Convert to float for processing, then back to binary
    voxel_float = voxel_array.astype(np.float32)

    # For binary arrays, use order=0 (nearest neighbor) to preserve binary nature
    # Calculate new dimensions
    original_shape = voxel_array.shape
    new_shape = list(original_shape)
    new_shape[compaction_axis] = int(original_shape[compaction_axis] * compaction_factor)

    # Ensure at least 1 voxel in compressed dimension
    new_shape[compaction_axis] = max(1, new_shape[compaction_axis])

    # Resize along compaction axis with nearest neighbor
    zoom_factors = [1.0, 1.0, 1.0]
    zoom_factors[compaction_axis] = new_shape[compaction_axis] / original_shape[compaction_axis]

    # Apply zoom with nearest neighbor interpolation
    compressed = zoom(voxel_float, zoom_factors, order=0)

    # Pad back to original shape
    result = np.zeros(original_shape, dtype=np.float32)

    if compaction_axis == 0:  # Z
        pad_before = (original_shape[0] - compressed.shape[0]) // 2
        result[pad_before:pad_before+compressed.shape[0], :, :] = compressed

    elif compaction_axis == 1:  # Y
        pad_before = (original_shape[1] - compressed.shape[1]) // 2
        result[:, pad_before:pad_before+compressed.shape[1], :] = compressed

    else:  # X
        pad_before = (original_shape[2] - compressed.shape[2]) // 2
        result[:, :, pad_before:pad_before+compressed.shape[2]] = compressed

    # Convert back to binary
    if preserve_binary:
        # Apply threshold to handle any floating point artifacts
        result = (result > threshold).astype(voxel_array.dtype)
    else:
        result = result.astype(voxel_array.dtype)

    return result

